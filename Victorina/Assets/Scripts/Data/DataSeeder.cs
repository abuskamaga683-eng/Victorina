using UnityEngine;
using Victorina.Core.Interfaces;
using Victorina.Core.Models;

namespace Victorina.Data
{
    public class DataSeeder
    {
        private readonly IQuestionRepository _questions;

        public DataSeeder(IQuestionRepository questions)
        {
            _questions = questions;
        }

        public void SeedIfEmpty()
        {
            Debug.Log("[DataSeeder] SeedIfEmpty — проверка вопросов");
            var all = _questions.GetAll();
            Debug.Log($"[DataSeeder] Вопросов в БД: {all.Count}");

            // 🔧 FIX: Обновляем pool_index для вопросов с невалидными значениями (переносим в "Общую базу")
            foreach (var q in all)
            {
                if (q.PoolIndex < 0 || q.PoolIndex >= PoolInfo.Count)
                {
                    q.PoolIndex = 0;
                    _questions.Update(q);
                    Debug.Log($"[DataSeeder] ✓ Fixed question id={q.Id}: pool_index -> 0");
                }
            }

            if (all.Count > 0)
            {
                bool hasValid = all.Exists(q => q.PoolIndex >= 0 && q.PoolIndex < PoolInfo.Count);
                if (hasValid) { Debug.Log("[DataSeeder] БД уже заполнена, пропускаем"); return; }

                Debug.LogWarning("[DataSeeder] Вопросы устарели (нет валидного pool_index) — удаляем и пересеиваем");
                foreach (var q in all) _questions.Delete(q.Id);
