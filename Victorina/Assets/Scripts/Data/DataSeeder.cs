                (0,
                 "Движение, ставшее популярным в США. Эти сообщества романтизируют и продвигают идею массовых убийств и, в особенности, массовых убийств среди детей и подростков в школах.",
                 "Скулшутинг"),

                (0,
                 "Что относится к персональным данным?",
                 "ФИО; номера документов (паспорт, СНИЛС, ИНН); банковские данные (номер счёта, карты, пин-код); контактная информация (телефон, email, адрес); фотографии и видеозаписи; данные о родственниках; логины и пароли."),

